def main() -> int:
    with open(DB_PATH, encoding="utf-8") as f:
        data = yaml.safe_load(f)

    EN_OUT.parent.mkdir(parents=True, exist_ok=True)
    KO_OUT.parent.mkdir(parents=True, exist_ok=True)
    EN_OUT.write_text(render_en(data), encoding="utf-8")
    KO_OUT.write_text(render_ko(data), encoding="utf-8")

    print(f"wrote {EN_OUT.relative_to(ROOT)}")
    print(f"wrote {KO_OUT.relative_to(ROOT)}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
