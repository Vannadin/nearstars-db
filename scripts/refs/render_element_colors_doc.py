        "주기율표 원소별 불꽃 반응 / 저온 플라즈마 색상 사전. Helmenstine 2017 "
        "차트(sciencenotes.org)는 장식용 그라디언트 때문에 정확한 hex 가 안 보여서, "
        "화학 문헌 + NIST 원자 스펙트럼 DB 를 1차 출처로 쓰고 차트는 시각 sanity "
