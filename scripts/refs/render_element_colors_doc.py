# db/refs/element_plasma_colors.yaml 에서 docs/reference/element-plasma-colors.md (영문 + ko 미러) 를 렌더
"""Generate the human-readable element plasma colors doc from the YAML DB.

Writes:
    docs/reference/element-plasma-colors.md       (English)
    ko/docs/reference/element-plasma-colors.md    (Korean mirror)

Layout: header + status legend + per-period tables. Each row shows
symbol, name, status, hex (with color swatch in inline HTML
`<span style="background:#hex">` for browsers), basis snippet.

Run after editing the YAML DB. Idempotent.
"""
from __future__ import annotations

import sys
from pathlib import Path

import yaml

ROOT = Path(__file__).resolve().parents[2]
DB_PATH = ROOT / "db" / "refs" / "element_plasma_colors.yaml"
EN_OUT = ROOT / "docs" / "reference" / "element-plasma-colors.md"
KO_OUT = ROOT / "ko" / "docs" / "reference" / "element-plasma-colors.md"


PERIOD_BOUNDS = [
    (1, "Period 1", (1, 2)),
    (2, "Period 2", (3, 10)),
    (3, "Period 3", (11, 18)),
    (4, "Period 4", (19, 36)),
    (5, "Period 5", (37, 54)),
    (6, "Period 6 (main + transition)", (55, 56, 72, 86)),   # Cs, Ba, then Hf..Rn skipping La..Lu
    (7, "Lanthanides", (57, 71)),
    (8, "Period 7 (main + transition)", (87, 88, 104, 118)), # Fr, Ra, then Rf..Og skipping Ac..Lr
    (9, "Actinides", (89, 103)),
]


def select_period_entries(data, period_idx, bounds):
    """Return entries whose atomic_number falls in the period range(s)."""
    if len(bounds) == 2:
        lo, hi = bounds
        return [(s, e) for s, e in data.items()
                if lo <= e["atomic_number"] <= hi]
    elif len(bounds) == 4:
        a, b, c, d = bounds
        return [(s, e) for s, e in data.items()
                if a <= e["atomic_number"] <= b or c <= e["atomic_number"] <= d]
    return []


def status_chip_en(status: str) -> str:
    return {
        "visible":              "visible",
        "no_flame_color":       "no flame color",
        "not_visible_to_humans": "not visible to humans",
        "too_radioactive":      "too radioactive",
        "too_short":            "half-life too short",
        "no_data":              "no published data",
    }[status]


def status_chip_ko(status: str) -> str:
    return {
        "visible":              "관측 가능",
        "no_flame_color":       "불꽃색 없음",
        "not_visible_to_humans": "가시광선 밖",
        "too_radioactive":      "방사능 위험",
        "too_short":            "반감기 부족",
        "no_data":              "자료 없음",
    }[status]


def swatch_cell(hex_val: str | None) -> str:
    if hex_val is None:
        return "—"
    return (f'<span style="display:inline-block;width:1em;height:1em;'
            f'background:{hex_val};border:1px solid #444;vertical-align:middle"></span> '
            f'`{hex_val}`')


def render_table(entries, status_chip):
    lines = ["| Z | Sym | Name | Color | Status | Basis |",
             "|---|-----|------|-------|--------|-------|"]
    entries.sort(key=lambda x: x[1]["atomic_number"])
    for sym, e in entries:
        lines.append(
            f"| {e['atomic_number']} "
            f"| **{sym}** "
            f"| {e['name']} "
            f"| {swatch_cell(e.get('hex'))} "
            f"| {status_chip(e['status'])} "
            f"| {e['basis']} |"
        )
    return "\n".join(lines)


def render_en(data) -> str:
    lines = [
        "# Element plasma colors",
        "",
        "Per-element flame test / low-temperature plasma colors. "
        "Replaces pixel-sampling of the Helmenstine 2017 chart (whose "
        "decorative gradient blurs the intended hex). Source: chemistry "
        "literature + NIST Atomic Spectra Database, with the Helmenstine "
        "chart as visual sanity check.",
        "",
        "Canonical source: `db/refs/element_plasma_colors.yaml`. "
        "This file is generated by `scripts/refs/render_element_colors_doc.py` — "
        "do not edit by hand.",
        "",
        "## Status legend",
        "",
        "| Status | Meaning | Hex |",
        "|---|---|---|",
        "| visible | Documented flame/plasma emission color | required |",
        "| no flame color | Element exists but no visible flame coloration | null |",
        "| not visible to humans | Emission falls outside 380–780 nm | null |",
        "| too radioactive | Flame test unsafe / not characterized | null |",
        "| half-life too short | Synthetic, decays before observation | null |",
        "| no published data | Literature silent | null |",
        "",
        "Reentry plasma at 8,000–15,000 K differs from atomic flame "
        "emission — molecular bands (N2+, C2 Swan, CN violet) dominate. "
        "See [composition-color reference](../../.claude/skills/firefly-cfg/references/composition-color.md) "
        "for bulk-gas reentry colors used in Firefly cfg.",
        "",
    ]
    for _, title, bounds in PERIOD_BOUNDS:
        entries = select_period_entries(data, None, bounds)
        if not entries:
            continue
        lines.append(f"## {title}")
        lines.append("")
        lines.append(render_table(entries, status_chip_en))
        lines.append("")
    lines.append("## Related")
    lines.append("")
    lines.append("- [composition-color (firefly-cfg skill)](../../.claude/skills/firefly-cfg/references/composition-color.md) — bulk-gas reentry plasma palette (different physics regime)")
    lines.append("- [phase3-mapping (firefly-cfg skill)](../../.claude/skills/firefly-cfg/references/phase3-mapping.md) — Phase 3 → Firefly field map")
    lines.append("")
    return "\n".join(lines)


